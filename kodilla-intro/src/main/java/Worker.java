public class Worker {
    String workerName;
    int workerAge;


    // constructor

    public Worker(String workerName, int workerAge) {
        this.workerName = workerName;
        this.workerAge = workerAge;
    }




    // getters, if needed:

    public String getWorkerName() {
        return workerName;
    }

    public int getWorkerAge() {
        return workerAge;
    }
}
